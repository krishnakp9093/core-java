package in.co.rays.basics;

public class Mixing_operators {
	public static void main(String[] args) {
		char c;
		int iv1 = 64;
		c = (char)iv1;
		System.out.println("c:"+ c);
		System.out.println("iv1:"+iv1);
		
		
		double fv1,fv2;
		int iv = 123;
		fv1 = iv/50;
		fv2 = iv/50.0;
		System.out.println("fv1:"+fv1);
	System.out.println("fv2:"+fv2);
		
		
		
	}
	
	
	
	
	

}
