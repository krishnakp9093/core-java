package in.co.OOP;

public class InheritanceShape {
	private String colour;
	private int bw;          //border width
	private int side;
	
	public void setColour(String colour) {
		this.colour = colour;
	}
    public String getColour() {
    	return colour;
    }
	
    public void setBw(int bw) {
    	this.bw= bw;
    }
	public int getBw() {
		return bw;
	}
	public void setSide(int side) {
		this.side= side ;
	}
	public int getSide() {
		return side;
	}
	
	
	
	
	
	
}
